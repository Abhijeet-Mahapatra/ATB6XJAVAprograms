package april.ex_18042024;

public class Lab035 {
    public static void main(String[] args) {

        // || - OR
        // True || True -> True
        // True || False -> True
        // False || True -> True
        // False || False -> False

        System.out.println(true || true);
        System.out.println(true || false);
        System.out.println(false || true);
        System.out.println(false || false);

    }
}
