package april.ex_18042024;

public class Lab044 {
    public static void main(String[] args) {

        //Increment (++) / Decrement (--) Operators

        //Pre , Post

        // Pre -> Value is incremented first and then the result is computed and assigned

        int a = 10;
        // ++a -> a = a+1 (Value increase first then assign the value)

        int b = ++a;  // a = a+1

        // b=11
        // a=11

        System.out.println(b);
        System.out.println(a);

    }
}
