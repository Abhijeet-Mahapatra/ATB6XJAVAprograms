package ex_18042024;

public class Lab036 {
    public static void main(String[] args) {
        // AND && // Only true && true returns true
        // true && true -> true
        // true && false -> false
        // false && true -> false
        // false && false -> false

        System.out.println(true && true);
        System.out.println(true && false);
        System.out.println(false && true);
        System.out.println(false && false);
    }
}
